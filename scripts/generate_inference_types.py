# Code emission
# ---------------------------------------------------------------------------


def _emit_type_class(
    class_name: str,
    props: List[SchemaProp],
    description: str,
) -> str:
    """Return source for a single typed DotDict subclass."""
    lines = [
        f"class {class_name}(DotDict):",
        f'    """{description}"""',
    ]
    if not props:
        lines.append("    ...")
        return "\n".join(lines) + "\n"

    for p in props:
        ptype = f"Optional[{p.py_type}]" if p.nullable else p.py_type
        comment = f"  # {p.description}" if p.description else ""
        lines.append(f"    {p.name}: {ptype}{comment}")

    return "\n".join(lines) + "\n"


def _emit_types_module(spec_text: str) -> Dict[str, str]:
    """Auto-discover and generate the full types module.

    Returns ``object_value`` → ``full.class.import.path`` for the type registry.
    """
    infos = _discover_types(spec_text)

    by_module: Dict[str, List[SchemaInfo]] = {}
    for info in infos:
        by_module.setdefault(info.module, []).append(info)

    object_type_map: Dict[str, str] = {}

    for module, schemas in sorted(by_module.items()):
        mod_dir = TYPES_ROOT / module
        mod_dir.mkdir(parents=True, exist_ok=True)

        classes: List[str] = []
        all_names: List[str] = []

        for info in schemas:
            props = _extract_schema_props(spec_text, info.schema_name)
            desc = f"Typed response for ``{info.schema_name}`` schema."
            classes.append(_emit_type_class(info.class_name, props, desc))
            all_names.append(info.class_name)

            if info.object_value:
                object_type_map[info.object_value] = (
                    f"pydo.types.{module}.{info.class_name}"
                )

        needs_list = any("List[" in c for c in classes)
        typing_imports = ["Any", "Optional"]
        if needs_list:
            typing_imports.append("List")

        module_text = (
            INIT_HEADER
            + "\n"
            + f"from typing import {', '.join(sorted(typing_imports))}\n\n"
            + "from pydo.custom_extensions import DotDict\n\n\n"
            + "\n\n".join(classes)
            + "\n"
            + f"__all__ = {all_names!r}\n"
        )
        (mod_dir / "__init__.py").write_text(module_text, encoding="utf-8")

    # Top-level __init__.py re-exporting everything
    all_top_level: List[str] = []
    import_lines: List[str] = []
    for module, schemas in sorted(by_module.items()):
        for info in schemas:
            import_lines.append(
                f"from pydo.types.{module} import {info.class_name}"
            )
            all_top_level.append(info.class_name)

    init_text = (
        INIT_HEADER
        + '\n"""Typed response models for inference endpoints (auto-generated).\n\n'
        + "Usage::\n\n"
        + "    from pydo.types.chat import ChatCompletion\n"
        + "    from pydo.types.images import ImagesResponse, Image\n"
        + "    from pydo.types import ChatCompletion  # also works\n"
        + '"""\n\n'
        + "\n".join(import_lines)
        + "\n\n"
        + f"__all__ = {all_top_level!r}\n"
    )
    TYPES_ROOT.mkdir(parents=True, exist_ok=True)
    (TYPES_ROOT / "__init__.py").write_text(init_text, encoding="utf-8")

    return object_type_map


def _write_type_registry(
    path: Path, object_type_map: Dict[str, str]
) -> None:
    """Emit ``_type_registry.py`` mapping ``object`` field values → typed classes."""
    lines = [
        INIT_HEADER,
        "",
        '"""Auto-dispatch: maps the ``object`` field in JSON responses to typed classes."""',
        "",
        "from typing import Dict, Type",
        "",
        "from pydo.custom_extensions import DotDict",
        "",
    ]
    import_set: Dict[str, List[str]] = {}
    for fqn in object_type_map.values():
        mod, cls = fqn.rsplit(".", 1)
        import_set.setdefault(mod, []).append(cls)

    for mod in sorted(import_set):
        classes = ", ".join(sorted(import_set[mod]))
        lines.append(f"from {mod} import {classes}")

    lines.append("")
    lines.append("")
    lines.append("_OBJECT_TYPE_MAP: Dict[str, Type[DotDict]] = {")
    for obj_val, fqn in sorted(object_type_map.items()):
        cls = fqn.rsplit(".", 1)[1]
        lines.append(f'    "{obj_val}": {cls},')
    lines.append("}")
    lines.append("")
    path.write_text("\n".join(lines), encoding="utf-8")


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------


def main() -> None:
    if not SPEC_PATH.is_file():
        raise SystemExit(
            f"OpenAPI spec not found at {SPEC_PATH}\n"
            "Place DigitalOcean-public.v2.yaml in the repo root."
        )

    spec_text = SPEC_PATH.read_text(encoding="utf-8")

    if TYPES_ROOT.exists():
        shutil.rmtree(TYPES_ROOT)

    object_type_map = _emit_types_module(spec_text)
    _write_type_registry(TYPES_ROOT / "_type_registry.py", object_type_map)

    # Summary
    infos = _discover_types(spec_text)
    by_mod: Dict[str, List[str]] = {}
    for info in infos:
        by_mod.setdefault(info.module, []).append(info.class_name)
    print(f"Generated {len(infos)} types in {TYPES_ROOT}:")
    for mod, classes in sorted(by_mod.items()):
        print(f"  pydo.types.{mod}: {', '.join(classes)}")
    if object_type_map:
        print(f"Type registry: {len(object_type_map)} auto-dispatch entries")


if __name__ == "__main__":
    main()
