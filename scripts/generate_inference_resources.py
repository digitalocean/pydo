def _immediate_child_segments(
    prefix: Tuple[str, ...], tree_keys: Set[Tuple[str, ...]]
) -> List[str]:
    plen = len(prefix)
    kids: Set[str] = set()
    for k in tree_keys:
        if len(k) == plen + 1 and k[:plen] == prefix:
            kids.add(k[-1])
    return sorted(kids)


def _write_namespace_init(
    init_path: Path,
    prefix: Tuple[str, ...],
    children: List[str],
    *,
    async_mode: bool,
) -> None:
    parent_seg = prefix[-1]
    parent_class = _pascal_class_name(parent_seg)
    imp = "from pydo.aio import Client" if async_mode else "from pydo import Client"
    needs_generate_alias = parent_seg == "images" and "generations" in children
    lines = [
        INIT_HEADER,
        "",
        imp,
    ]
    if needs_generate_alias:
        lines.append("from typing import Any")
    for c in children:
        lines.append(f"from .{c} import {_pascal_class_name(c)}")
    lines.extend(
        [
            "",
            "",
            f"class {parent_class}:",
            '    """Inference path namespace (generated)."""',
            "",
            "    def __init__(self, client: Client) -> None:",
        ]
    )
    for c in children:
        cc = _pascal_class_name(c)
        lines.append(f"        self.{c} = {cc}(client)")
    if needs_generate_alias:
        async_kw = "async " if async_mode else ""
        ret_kw = "await " if async_mode else ""
        lines.append("")
        lines.append(f"    {async_kw}def generate(self, **kwargs: Any) -> Any:")
        lines.append(
            '        """Alias for ``images.generations.generate`` '
            '(``POST /v1/images/generations``)."""'
        )
        lines.append(
            f"        return {ret_kw}self.generations.generate(**kwargs)\n"
        )
    lines.append("")
    lines.append(f'__all__ = ["{parent_class}"]')
    lines.append("")
    init_path.write_text("\n".join(lines), encoding="utf-8")


def _collect_ops_by_group(
    builders: List[Tuple[str, str, str]],
) -> Dict[str, List[OpSpec]]:
    by_group: Dict[str, List[OpSpec]] = {}
    for group, op_method, url in builders:
        segs = _path_segments(url)
        if not segs:
            raise RuntimeError(f"no segments for {url!r} ({group}.{op_method})")
        by_group.setdefault(group, []).append(
            OpSpec(group=group, op_method=op_method, segments=segs)
        )
    return by_group


def _build_tree_keys(specs: List[OpSpec]) -> Set[Tuple[str, ...]]:
    keys: Set[Tuple[str, ...]] = set()
    for s in specs:
        for i in range(len(s.segments)):
            keys.add(tuple(s.segments[: i + 1]))
    return keys


def _write_package_init_with_facade(
    init_path: Path,
    top_segments: List[str],
    *,
    class_name: str,
    async_mode: bool,
) -> None:
    """Write package ``__init__.py`` with the top-level resource root class."""
    imp = "from pydo.aio import Client" if async_mode else "from pydo import Client"
    lines = [
        INIT_HEADER,
        "",
        imp,
    ]
    for seg in sorted(top_segments):
        cls = _pascal_class_name(seg)
        lines.append(f"from .{seg} import {cls}")
    lines.extend(
        [
            "",
            "",
            f"class {class_name}:",
            '    """Nested inference resource root (generated)."""',
            "",
            "    def __init__(self, client: Client) -> None:",
        ]
    )
    for seg in sorted(top_segments):
        cls = _pascal_class_name(seg)
        lines.append(f"        self.{seg} = {cls}(client)")
    lines.append("")
    lines.append(f'__all__ = ["{class_name}"]')
    lines.append("")
    init_path.write_text("\n".join(lines), encoding="utf-8")


def _write_empty_package_init(base: Path, *, class_name: str, async_mode: bool) -> None:
    base.mkdir(parents=True, exist_ok=True)
    imp = "from pydo.aio import Client" if async_mode else "from pydo import Client"
    (base / "__init__.py").write_text(
        INIT_HEADER
        + "\n"
        + f"{imp}\n"
        + "\n\n"
        + f"class {class_name}:\n"
        + '    """Nested inference resource root (no operations in spec)."""\n'
        + "\n"
        + "    def __init__(self, client: Client) -> None:\n"
        + "        self._client = client\n"
        + "\n"
        + f'__all__ = ["{class_name}"]\n',
        encoding="utf-8",
    )


def _emit_group(
    base: Path,
    specs: List[OpSpec],
    *,
    facade_class: str,
    async_mode: bool,
) -> List[str]:
    """Return sorted top-level URL segments (first path component after ``/v1``)."""
    if not specs:
        _write_empty_package_init(base, class_name=facade_class, async_mode=async_mode)
        return []

    leaf_by_path: Dict[Tuple[str, ...], OpSpec] = {}
    for s in specs:
        key = tuple(s.segments)
        if key in leaf_by_path:
            raise RuntimeError(
                f"duplicate inference path {key!r}: "
                f"{leaf_by_path[key].op_method} vs {s.op_method}"
            )
        leaf_by_path[key] = s

    tree_keys = _build_tree_keys(specs)

    for path_tuple, spec in leaf_by_path.items():
        *parents, leaf_seg = path_tuple
        leaf_class = _pascal_class_name(leaf_seg)
        if parents:
            rel = Path(*parents) / f"{leaf_seg}.py"
        else:
            rel = Path(f"{leaf_seg}.py")
        if (
            not parents
            and leaf_seg == "async_invoke"
            and spec.op_method == "create_async_invoke"
        ):
            _write_async_invoke_leaf_class(
                base / rel,
                group=spec.group,
                async_mode=async_mode,
            )
        else:
            _write_leaf_class(
                base / rel,
                group=spec.group,
                op_method=spec.op_method,
                facade_method=_facade_method_name(spec.op_method),
                class_name=leaf_class,
                async_mode=async_mode,
            )

    for prefix in sorted(tree_keys, key=lambda x: len(x), reverse=True):
        if prefix in leaf_by_path:
            continue
        children = _immediate_child_segments(prefix, tree_keys)
        if not children:
            continue
        init_dir = base.joinpath(*prefix)
        init_dir.mkdir(parents=True, exist_ok=True)
        _write_namespace_init(
            init_dir / "__init__.py",
            prefix,
            children,
            async_mode=async_mode,
        )

    top_segments = sorted({s.segments[0] for s in specs})
    _write_package_init_with_facade(
        base / "__init__.py",
        top_segments,
        class_name=facade_class,
        async_mode=async_mode,
    )
    return top_segments


def _write_client_surface(
    path: Path,
    inference_top_segments: List[str],
) -> None:
    """Emit mixin classes merged into :class:`pydo.Client` (sync and async both use this file)."""
    lines: List[str] = [
        INIT_HEADER,
        "",
        "from typing import Any",
        "",
        "",
        "class InferenceClientSurface:",
        '    """Generated shortcuts for serverless inference (discovered from the published spec)."""',
        "",
    ]
    # ``images`` is handled manually in Client.__init__ (reassigned after
    # GeneratedClient sets self.images = ImagesOperations).  A @property
    # would block that assignment since it has no setter.
    skip_surface_segments = frozenset({"images"})

    for seg in inference_top_segments:
        if not seg.isidentifier() or seg in skip_surface_segments:
            continue
        lines.append("    @property")
        lines.append(f"    def {seg}(self) -> Any:")
        lines.append(
            f'        """Nested serverless inference API under path segment ``{seg}``."""\n'
        )
        lines.append(f"        return self._require_inference_resource_root().{seg}\n")
    if "async_invoke" in inference_top_segments:
        lines.append("    @property")
        lines.append("    def audio(self) -> Any:")
        lines.append(
            '        """Audio namespace (TTS + generation via async-invoke)."""\n'
        )
        lines.append(
            "        return self._require_inference_resource_root().async_invoke.audio\n"
        )
        lines.append("    @property")
        lines.append("    def speech(self) -> Any:")
        lines.append(
            '        """TTS entry: same as ``audio.speech`` (async-invoke)."""\n'
        )
        lines.append(
            "        return self._require_inference_resource_root().async_invoke.audio.speech\n"
        )
        lines.append("    @property")
        lines.append("    def async_images(self) -> Any:")
        lines.append(
            '        """Async image generation: same as ``async_invoke.images``."""\n'
        )
        lines.append(
            "        return self._require_inference_resource_root().async_invoke.images\n"
        )
    if "images" in inference_top_segments:
        lines.append("    @property")
        lines.append("    def image_generations(self) -> Any:")
        lines.append(
            '        """Explicit ``images.generations`` (sync ``POST /v1/images/generations``)."""\n'
        )
        lines.append(
            "        return self._require_inference_resource_root().images.generations\n"
        )
    lines.append("    @property")
    lines.append("    def inference_layout(self) -> Any:")
    lines.append(
        '        """Full nested serverless inference layout (same objects as the shortcuts above)."""\n'
    )
    lines.append("        return self._require_inference_resource_root()\n")
    lines.extend(
        [
            "",
            "",
            "class AgentClientSurface:",
            '    """Generated shortcut for agent inference operations."""',
            "",
            "    @property",
            "    def agent(self) -> Any:",
            '        """Nested agent inference API (``agent_inference`` operation group)."""\n',
            "        return self._require_agent_inference_resource_root()",
            "",
            "",
            '__all__ = ["AgentClientSurface", "InferenceClientSurface"]',
            "",
        ]
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


def _write_inference_surface_manifest(
    path: Path,
    by_group: Dict[str, List[OpSpec]],
) -> None:
    """Machine-readable list of discovered inference operations (for docs / tooling)."""
    payload: Dict[str, Any] = {
        "source": "operations/_operations.py build_inference_* / build_agent_inference_*",
        "inference": [],
        "agent_inference": [],
    }
    for spec in by_group.get("inference", []):
        payload["inference"].append(
            {
                "op_method": spec.op_method,
                "path_segments": spec.segments,
                "facade_method": _facade_method_name(spec.op_method),
            }
        )
    for spec in by_group.get("agent_inference", []):
        payload["agent_inference"].append(
            {
                "op_method": spec.op_method,
                "path_segments": spec.segments,
                "facade_method": _facade_method_name(spec.op_method),
            }
        )
    path.write_text(json.dumps(payload, indent=2) + "\n", encoding="utf-8")


def main() -> None:
    if not OPS_PATH.is_file():
        raise SystemExit(f"missing {OPS_PATH}")

    if OUT_ROOT.exists():
        shutil.rmtree(OUT_ROOT)

    builders = _parse_builders(OPS_PATH)
    by_group = _collect_ops_by_group(builders)

    OUT_ROOT.mkdir(parents=True, exist_ok=True)

    inference_top = _emit_group(
        OUT_ROOT / "inference",
        by_group.get("inference", []),
        facade_class="InferenceResources",
        async_mode=False,
    )
    _emit_group(
        OUT_ROOT / "agent_inference",
        by_group.get("agent_inference", []),
        facade_class="AgentInferenceResources",
        async_mode=False,
    )
    _write_client_surface(OUT_ROOT / "client_surface.py", inference_top)
    _write_inference_surface_manifest(OUT_ROOT / "inference_surface.json", by_group)

    aio_root = OUT_ROOT / "aio"
    aio_root.mkdir(parents=True, exist_ok=True)
    _emit_group(
        aio_root / "inference",
        by_group.get("inference", []),
        facade_class="InferenceResources",
        async_mode=True,
    )
    _emit_group(
        aio_root / "agent_inference",
        by_group.get("agent_inference", []),
        facade_class="AgentInferenceResources",
        async_mode=True,
    )
    (aio_root / "__init__.py").write_text(
        INIT_HEADER
        + '\n"""Async inference resource packages (e.g. ``pydo.resources.aio.inference``)."""\n'
        "\n"
        "__all__ = []\n",
        encoding="utf-8",
    )

    (OUT_ROOT / "__init__.py").write_text(
        INIT_HEADER
        + '\n"""Nested inference packages: ``inference/``, ``agent_inference/``, ``aio/``."""\n'
        "\n"
        "__all__ = []\n",
        encoding="utf-8",
    )


if __name__ == "__main__":
    main()
