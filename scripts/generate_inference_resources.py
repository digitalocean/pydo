"""Emit nested ``src/pydo/resources/`` packages for inference HTTP paths.

**Source of truth:** only ``build_inference_*`` / ``build_agent_inference_*``
helpers in ``operations/_operations.py`` (Autorest output from the OpenAPI
spec).  Droplet / v2 APIs are not read here.

For each inference URL, path segments become nested modules/classes; method
names map from operation names (``list_*`` → ``list``, ``create_image`` →
``generate``, etc.).  Keyword arguments on generated helpers are passed into
the JSON body (except SDK kwargs like ``headers``, ``cls``), matching the
schema Autorest embedded for each operation.

Also emits ``client_surface*.py`` mixins so :class:`pydo.Client` shortcuts
(``chat``, ``models``, ``inference_layout``, ``agent``, …) track whatever the
spec contains — no hand-maintained list in ``_patch.py``.

Shortcuts on :class:`pydo.Client`: use ``client.image_generations`` (or
``client.inference_layout.images``) for ``POST /v1/images/generations`` — we do **not**
expose ``client.images`` for inference because that name is reserved for the DO v2 Images
API. ``client.audio`` / ``client.speech`` / ``client.async_images`` mirror familiar
paths; ``async_invoke`` still exposes ``.images`` / ``.audio`` under the layout.

Run after Autorest (``make generate``).
"""
from __future__ import annotations

import json
import re
import shutil
from dataclasses import dataclass
from pathlib import Path
from typing import Any, Dict, List, Set, Tuple

ROOT = Path(__file__).resolve().parents[1]
OPS_PATH = ROOT / "src/pydo/operations/_operations.py"
OUT_ROOT = ROOT / "src/pydo/resources"

BUILD_RE = re.compile(
    r"^def build_(?P<group>inference|agent_inference)_(?P<op>.+)_request\b"
)
URL_RE = re.compile(r'_url\s*=\s*["\'](?P<url>[^"\']+)["\']')

_SDK_KWARGS = frozenset(
    {
        "headers",
        "params",
        "content_type",
        "cls",
        "raw_request_hook",
        "raw_response_hook",
        "polling",
        "continuation_token",
        "form_content_type",
        "error_map",
    }
)


def _header(*, async_mode: bool) -> str:
    client_import = (
        "from pydo.aio import Client" if async_mode else "from pydo import Client"
    )
    return (
        "# ------------------------------------\n"
        "# Copyright (c) DigitalOcean.\n"
        "# Licensed under the Apache-2.0 License.\n"
        "# ------------------------------------\n"
        "# Generated by scripts/generate_inference_resources.py — do not edit.\n"
        "from __future__ import annotations\n"
        "\n"
        "from typing import Any\n"
        "\n"
        f"{client_import}\n"
        "from pydo.custom_extensions import _wrap\n"
    )


INIT_HEADER = (
    "# ------------------------------------\n"
    "# Copyright (c) DigitalOcean.\n"
    "# Licensed under the Apache-2.0 License.\n"
    "# ------------------------------------\n"
    "# Generated by scripts/generate_inference_resources.py — do not edit.\n"
    "from __future__ import annotations\n"
)


def _path_segments(url: str) -> List[str]:
    parts = [p for p in url.split("/") if p]
    while parts:
        if parts[0] == "api" and len(parts) >= 2 and parts[1] == "v1":
            parts = parts[2:]
        elif parts[0] == "v1":
            parts = parts[1:]
        else:
            break
    return [re.sub(r"[^a-zA-Z0-9_]", "_", s) for s in parts]


def _parse_builders(path: Path) -> List[Tuple[str, str, str]]:
    lines = path.read_text(encoding="utf-8").splitlines(keepends=True)
    out: List[Tuple[str, str, str]] = []
    i = 0
    n = len(lines)
    while i < n:
        m = BUILD_RE.match(lines[i])
        if not m:
            i += 1
            continue
        group, op = m.group("group"), m.group("op")
        i += 1
        block: List[str] = []
        while i < n:
            line = lines[i]
            if line.startswith("def ") or line.startswith("class "):
                break
            block.append(line)
            i += 1
        um = URL_RE.search("".join(block))
        if not um:
            raise RuntimeError(f"No _url found in build_{group}_{op}_request")
        out.append((group, op, um.group("url")))
    return out


def _client_attr(group: str) -> str:
    return "agent_inference" if group == "agent_inference" else "inference"


def _facade_method_name(op_method: str) -> str:
    if op_method == "create_image":
        return "generate"
    if op_method.startswith("list_"):
        return "list"
    if op_method.startswith("create_"):
        return "create"
    if op_method.startswith("get_"):
        return "get"
    if op_method.startswith("delete_"):
        return "delete"
    if op_method.startswith("update_"):
        return "update"
    if op_method.startswith("patch_"):
        return "patch"
    return op_method


def _operation_has_json_body(op_method: str) -> bool:
    if op_method.startswith("list_") or op_method.startswith("get_"):
        return False
    if op_method.startswith("delete_"):
        return False
    return True


def _pascal_class_name(segment: str) -> str:
    parts = re.split(r"[_\-]+", segment)
    return "".join((p[:1].upper() + p[1:].lower()) if p else "" for p in parts if p)


@dataclass(frozen=True)
class OpSpec:
    group: str
    op_method: str
    segments: List[str]


def _write_leaf_class(
    path: Path,
    *,
    group: str,
    op_method: str,
    facade_method: str,
    class_name: str,
    async_mode: bool,
) -> None:
    ops = _client_attr(group)
    sdk_set = ", ".join(repr(x) for x in sorted(_SDK_KWARGS))

    if _operation_has_json_body(op_method):
        if async_mode:
            method_body = f"""        body = {{k: v for k, v in kwargs.items() if k not in _SDK_KWARGS}}
        sdk = {{k: v for k, v in kwargs.items() if k in _SDK_KWARGS}}
        return _wrap(await self._client.{ops}.{op_method}(body, **sdk))"""
        else:
            method_body = f"""        body = {{k: v for k, v in kwargs.items() if k not in _SDK_KWARGS}}
        sdk = {{k: v for k, v in kwargs.items() if k in _SDK_KWARGS}}
        return _wrap(self._client.{ops}.{op_method}(body, **sdk))"""
    else:
        if async_mode:
            method_body = (
                f"        return _wrap(await self._client.{ops}.{op_method}(**kwargs))"
            )
        else:
            method_body = f"        return _wrap(self._client.{ops}.{op_method}(**kwargs))"

    async_kw = "async " if async_mode else ""

    text = (
        _header(async_mode=async_mode)
        + "\n"
        + f"_SDK_KWARGS = frozenset({{{sdk_set}}})\n"
        + "\n\n"
        + f"class {class_name}:\n"
        + '    """Inference resource helper (generated)."""\n'
        + "\n"
        + "    def __init__(self, client: Client) -> None:\n"
        + "        self._client = client\n"
        + "\n"
        + f"    {async_kw}def {facade_method}(self, **kwargs: Any) -> Any:\n"
        + f'        """Delegates to ``client.{ops}.{op_method}``."""\n'
        + method_body
        + "\n"
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


